// Arithmetic Operator
    let a = 10;
    let b = 5;
    console.log((a + b)); // 15
    console.log(a ** b); // 100000
    console.log(a % b); // 0

    // Assignment Operator
    let x = 10;
    x += 5; // x = x + 5
    console.log(x); // 15
    x -= 3; // x = x - 3
    console.log(x); // 12
    x *= 2; // x = x * 2
    console.log(x); // 24
    x /= 4; // x = x / 4
    console.log(x); // 6

    // Comparison Operator
    console.log(a == b); // false
    console.log(3 == '3'); // true
    console.log(a === b); // false

    // Logical Operator
    console.log('cow' && 'cat'); // 'cat'
    console.log('sajol' && 'shamim'); // 'shamim'
    
    console.log(false || true); // true
    console.log(true || false); // true
    console.log(false || false); // false

    console.log('cow' || 'cat'); // 'cow'